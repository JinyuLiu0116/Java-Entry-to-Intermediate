public class arthmetic_expressions {
    public static void main(String[] args) {
        int friend = 10;
        System.out.println(friend);

        friend = friend + 1;
        System.out.println(friend);

        friend += 1;
        System.out.println(friend);

        friend = friend - 2;
        System.out.println(friend);

        friend -= 2;
        System.out.println(friend);

        friend ++;
        System.out.println(friend);

        friend *=2;
        System.out.println(friend);

        friend --;
        System.out.println(friend);

        double newFriend;
        newFriend = (double) friend/3;
        System.out.println(newFriend);


    }
}
